#!/usr/bin/env python3
"""Generate plots from saved analyzer JSON reports.

Usage examples:
    python tools/plot_analysis.py --input report.json --plot all
    python tools/plot_analysis.py --input reports/*.json --plot overlap-fraction
    python tools/plot_analysis.py --input reports --plot structural-effect-size \
        --metric participation_coefficient
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

# Allow `python tools/plot_analysis.py` from project root
PROJECT_ROOT = Path(__file__).resolve().parents[1]
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from src.analyzer.plotting import (
    METRIC_CHOICES,
    load_reports,
    plot_all,
    plot_overlap_fraction,
    plot_structural_effect_size,
    plot_structural_medians,
    plot_temporal_stability,
    resolve_report_paths,
)
from src.analyzer.config import AnalyzerConfigManager


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Plot analyzer JSON reports generated by tools/analyze.py --save-json",
    )
    parser.add_argument(
        "--config",
        default="config/analyzer.yaml",
        help="Analyzer YAML config path (default: config/analyzer.yaml)",
    )
    parser.add_argument(
        "--input",
        "-i",
        nargs="+",
        required=False,
        help="Input file(s), directory, and/or glob pattern(s)",
    )
    parser.add_argument(
        "--report-group",
        default=None,
        help="Report group from config plotting.report_groups",
    )
    parser.add_argument(
        "--plot",
        default=None,
        choices=(
            "all",
            "overlap-fraction",
            "temporal-stability",
            "structural-effect-size",
            "structural-medians",
        ),
        help="Which plot type to generate",
    )
    parser.add_argument(
        "--metric",
        default=None,
        choices=METRIC_CHOICES,
        help="Structural metric to use for structural plot types",
    )
    parser.add_argument(
        "--output-dir",
        "-o",
        default=None,
        help="Directory where images are written",
    )
    args = parser.parse_args()

    cfg = AnalyzerConfigManager(args.config)
    cfg_plot = cfg.plotting()
    cfg_dirs = cfg.directories()

    plot_name = args.plot or str(cfg_plot.get("default_plot", "all"))
    metric_name = args.metric or str(
        cfg_plot.get("default_metric", "participation_coefficient")
    )
    out_dir = Path(args.output_dir) if args.output_dir else cfg_dirs["plot_output_dir"]

    if metric_name not in METRIC_CHOICES:
        print(
            f"Error: unsupported metric '{metric_name}'. Choices: {', '.join(METRIC_CHOICES)}",
            file=sys.stderr,
        )
        sys.exit(1)

    if plot_name not in {
        "all",
        "overlap-fraction",
        "temporal-stability",
        "structural-effect-size",
        "structural-medians",
    }:
        print(
            "Error: unsupported plot type. "
            "Choices: all, overlap-fraction, temporal-stability, structural-effect-size, structural-medians",
            file=sys.stderr,
        )
        sys.exit(1)

    inputs = []
    if args.input:
        inputs = args.input
    elif args.report_group:
        inputs = cfg.report_group(args.report_group)
    else:
        default_group = cfg_plot.get("default_report_group", "")
        if isinstance(default_group, str) and default_group:
            inputs = cfg.report_group(default_group)
        if not inputs:
            inputs = [str(cfg_dirs["report_dir"] / "*.json")]

    paths = resolve_report_paths(inputs)
    if not paths:
        print("Error: no report files found", file=sys.stderr)
        sys.exit(1)

    try:
        reports = load_reports(paths)
    except Exception as exc:
        print(f"Error loading reports: {exc}", file=sys.stderr)
        sys.exit(1)

    out_dir.mkdir(parents=True, exist_ok=True)

    generated = []
    if plot_name == "all":
        generated = plot_all(reports, out_dir=out_dir, metric_name=metric_name)
    elif plot_name == "overlap-fraction":
        path = plot_overlap_fraction(reports, out_dir=out_dir)
        generated = [path] if path is not None else []
    elif plot_name == "temporal-stability":
        path = plot_temporal_stability(reports, out_dir=out_dir)
        generated = [path] if path is not None else []
    elif plot_name == "structural-effect-size":
        generated = plot_structural_effect_size(
            reports,
            out_dir=out_dir,
            metric_name=metric_name,
        )
    elif plot_name == "structural-medians":
        generated = plot_structural_medians(
            reports,
            out_dir=out_dir,
            metric_name=metric_name,
        )

    if not generated:
        print(
            "No plots generated. Reports may be metadata-only (v1) or missing required fields.",
            file=sys.stderr,
        )
        sys.exit(1)

    for path in generated:
        print(f"✓ {path}")


if __name__ == "__main__":
    main()
